from pydantic import BaseModel, Field, field_validator, ConfigDict
from typing import Optional, Any
from datetime import datetime
from enum import Enum
import uuid


# =============================================================================
# ENUMS
# =============================================================================

class TaskStatus(str, Enum):
    """
    Possible statuses for a task or subtask.
    Using an enum means FastAPI will reject any status that isn't one of these.
    """
    NOT_STARTED = "not_started"
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"


# =============================================================================
# TASK & SUBTASK SCHEMAS
# =============================================================================
#
# Two IDs per task — see Tables.py header comment for full explanation.
#   ai_id: Simple ID from AI (e.g., "task_1", "task_3a") — always present
#   id: UUID from backend — None until saved to database
#
# =============================================================================

class SubTask(BaseModel):
    """
    A subtask belonging to a parent task.
    
    Subtasks are simpler than tasks — no requires_input, no nested subtasks.
    They're just ordered steps within a task.
    
    Example:
        {
            "ai_id": "task_3a",
            "id": "a1b2c3d4-...",     ← None before saving to DB
            "description": "Inflate the tube slightly",
            "order": 1,
            "status": "not_started"
        }
    """
    ai_id: str = Field(
        ...,
        description="Simple AI-generated ID for tracking (e.g., 'task_3a')"
    )
    id: Optional[str] = Field(
        default=None,
        description="UUID — generated by backend when saved to DB. None before that."
    )
    description: str = Field(
        ...,
        description="What the user needs to do for this subtask"
    )
    order: int = Field(
        ...,
        description="Position within parent task's subtask list (1-indexed)"
    )
    status: TaskStatus = Field(
        default=TaskStatus.NOT_STARTED,
        description="Current status of the subtask"
    )
    
    model_config = ConfigDict(extra="forbid")

    @field_validator('ai_id')
    @classmethod
    def validate_ai_id(cls, v: str) -> str:
        """Make sure ai_id isn't empty or just whitespace."""
        if not v or len(v.strip()) == 0:
            raise ValueError("ai_id cannot be empty")
        return v.strip()


class Task(BaseModel):
    """
    A top-level task in a goal's ordered task list.
    
    Tasks are the main steps the user follows to achieve their goal.
    Each task can optionally contain subtasks for more granular steps.
    
    Example:
        {
            "ai_id": "task_3",
            "id": "550e8400-...",          ← None before saving to DB
            "description": "Find the puncture",
            "order": 3,
            "status": "not_started",
            "requires_input": false,
            "subtasks": [
                {"ai_id": "task_3a", "description": "Inflate tube", "order": 1, ...},
                {"ai_id": "task_3b", "description": "Check for bubbles", "order": 2, ...}
            ]
        }
    """
    ai_id: str = Field(
        ...,
        description="Simple AI-generated ID for tracking (e.g., 'task_1')"
    )
    id: Optional[str] = Field(
        default=None,
        description="UUID — generated by backend when saved to DB. None before that."
    )
    description: str = Field(
        ...,
        description="What the user needs to do for this task"
    )
    order: int = Field(
        ...,
        description="Position in the goal's task list (1-indexed)"
    )
    status: TaskStatus = Field(
        default=TaskStatus.NOT_STARTED,
        description="Current status of the task"
    )
    requires_input: bool = Field(
        default=False,
        description="If true, this task needs user input before AI can plan further. "
                    "E.g., 'Check eligibility requirements' — user must report back."
    )
    subtasks: list[SubTask] = Field(
        default_factory=list,
        description="Ordered list of subtasks (can be empty if not expanded yet)"
    )

    model_config = ConfigDict(extra="forbid")

    @field_validator('ai_id')
    @classmethod
    def validate_ai_id(cls, v: str) -> str:
        if not v or len(v.strip()) == 0:
            raise ValueError("ai_id cannot be empty")
        return v.strip()


